VOID EQA_Girin(EQS_EVENT *pInEvent)
{
	switch (pInEvent->EventID)
	{
	case EQE_APP_START:
        EQS_GetDC(TRUE);

        ScrollList(Where, 2, 2, 8, 8, 10, 5, 0, SCROLL_DEFAULTS);
		CmdConsole(CONSOLE_TYPE_SEL_CANCEL, CMD_DEFAULTS);
		break;

	case EQE_APP_END:
        EQS_ReleaseDC();
        break;

    case EQE_IO_SCROLL_LIST_SELECT:
        
        switch (pInEvent->dwParam)
        {
        case 0: // Puchon.
            PopUpMenu(Blocks, 3, 0, POPUP_DEFAULTS);
            break;
        }
        break;

    case EQE_IO_POPUP_SELECT:

        switch (pInEvent->dwParam)
        {
        case 0:
            EQS_End();
            break;
        }

        break;

	default:
		EQS_DefEventProc(pInEvent);
		break;
	}
}

